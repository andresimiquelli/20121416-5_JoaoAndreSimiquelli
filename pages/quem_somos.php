<h1>Quem somos</h1>
<p>
    A <strong>Doce de Minas</strong> surgiu da vontade de levar a doce tradição mineira a todo o Brasil. Uma empresa familiar que preserva o costume e a qualidade nos seus produtos.
</p>
<p>
    Tudo começou com o Sr. Chico doceiro, como é conhecido na região, o Sr. Francisco Assis.
     Desde jovem, aprendeu com sua avó a fazer deliciosos doces, conhecimento que sempre fez questão de transmitir a filhos e netos. 
     Em 2001, com o aumento do turismo em Tiradentes, os doces do Seu Chico começaram a fazer sucesso entre os turistas. 
</p>
